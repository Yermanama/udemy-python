# Convertir un tipo entero, incluyendo base

# Base 10

a = int('23', 10)
print(f'a en base 10 -> {a}')

# Base binario

a = int('10111', 2)
print(f'a en binario -> {a}')

# base octal 

a = int('27', 8)
print(f'a en octal -> {a}')

# base hexadecimal
a = int('17', 16)
print(f'a en octal -> {a}')

