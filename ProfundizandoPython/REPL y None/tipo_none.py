# None - Significa ausencia de valor

variable = None

# Como vamos a ver en este bloque de código, un tipo none se considera de tipo falso
if variable:
    print('Se considera verdadera')
else:
    print('Se considera falsa')

'''
Ocho formas de generar un bool falso:
1. Camilla simple o doble (Cadena vacía)
2. Lista vacía 
3. Tupla vacía
4. Diccionario vacío
5. Entero 0
6. Flotante 0.0
7. Bool False
8. Tipo None'''