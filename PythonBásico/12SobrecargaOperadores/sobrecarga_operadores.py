# operador +
# Con tipos numéricos hacer la suma
a = 2 
b = 3
print(a + b)
# Con strings hace concatenación
a = 'hola'
b = 'mundo'
print(a + b)
# Con cadenas hace la unión de las dos listas
a = [1,2,3]
b = [4,5,6]
print(a + b)
# El operador + se comporta de diferente manera según los datos con los que trabaja, y esto 
# lo podemos modificar nosotros, con una clase, para que actúe de determinada con nuestros
# objetos
